CREATE TABLE EXPENSE (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EXPENSE_NAME VARCHAR(50) NOT NULL CHECK(EXPENSE_NAME <> ''),
    AMOUNT NUMERIC NOT NULL,
    EXPENSE_DESCRIPTION VARCHAR(180),
    EXPENSE_DATE TIMESTAMP NOT NULL,
    EXPENSE_CATEGORY BIGINT NOT NULL,
    FIN_ACCOUNT BIGINT NOT NULL,
    PAYMENT_TYPE BIGINT NOT NULL,
    CREATED_BY BIGINT NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    FOREIGN KEY (CREATED_BY) REFERENCES PERSON (ID),
    FOREIGN KEY (EXPENSE_CATEGORY) REFERENCES EXPENSE_CATEGORY (ID),
    FOREIGN KEY (FIN_ACCOUNT) REFERENCES FINANCIAL_ACCOUNT (ID),
    FOREIGN KEY (PAYMENT_TYPE) REFERENCES PAYMENT_TYPE (ID)
);