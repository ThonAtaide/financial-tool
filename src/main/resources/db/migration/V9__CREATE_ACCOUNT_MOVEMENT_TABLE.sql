CREATE TABLE ACCOUNT_MOVEMENT
(
    ID                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MOVEMENT_NAME        VARCHAR(50) NOT NULL CHECK (MOVEMENT_NAME <> ''),
    MOVEMENT_DESCRIPTION VARCHAR(180),
    AMOUNT               NUMERIC     NOT NULL,
    MOVEMENT_DATE        TIMESTAMP   NOT NULL,
    FIN_ACCOUNT          BIGINT      NOT NULL,
    MOVEMENT_TYPE        VARCHAR(50) NOT NULL, --DEPOSITO - DESPESA - TRANSFERENCIA
    CREATED_BY           BIGINT      NOT NULL,
    CREATED_AT           TIMESTAMP   NOT NULL,
    UPDATED_AT           TIMESTAMP   NOT NULL,
    FOREIGN KEY (CREATED_BY) REFERENCES PERSON (ID),
    FOREIGN KEY (FIN_ACCOUNT) REFERENCES FINANCIAL_ACCOUNT (ID)
);