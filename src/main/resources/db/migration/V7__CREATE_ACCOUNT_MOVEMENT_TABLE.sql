CREATE TABLE ACCOUNT_MOVEMENT (
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   DESCRIPTION VARCHAR(180),
   AMOUNT NUMERIC NOT NULL,
   ACCOUNT BIGINT NOT NULL,
   REAL_DATE TIMESTAMP NOT NULL,
   MOVEMENT_DIRECTION VARCHAR(50) NOT NULL CHECK (MOVEMENT_DIRECTION <> ''),
   CREATED_BY BIGINT NOT NULL,
   CREATED_AT TIMESTAMP NOT NULL,
   UPDATED_AT TIMESTAMP NOT NULL,
   FOREIGN KEY (ACCOUNT) REFERENCES FINANCIAL_ACCOUNT (ID),
   FOREIGN KEY (CREATED_BY) REFERENCES PERSON (ID)
);