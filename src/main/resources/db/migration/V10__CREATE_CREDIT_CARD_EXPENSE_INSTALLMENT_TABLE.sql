CREATE TABLE CREDIT_CARD_EXPENSE_INSTALLMENT (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CREDIT_CARD_EXPENSE BIGINT NOT NULL,
    INSTALLMENT_DATE TIMESTAMP NOT NULL,
    INSTALLMENT_NUMBER INT NOT NULL,
    AMOUNT NUMERIC NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    FOREIGN KEY (CREDIT_CARD_EXPENSE) REFERENCES CREDIT_CARD_EXPENSE (ID),
    unique (CREDIT_CARD_EXPENSE, INSTALLMENT_NUMBER),
    unique (CREDIT_CARD_EXPENSE, INSTALLMENT_DATE)
);