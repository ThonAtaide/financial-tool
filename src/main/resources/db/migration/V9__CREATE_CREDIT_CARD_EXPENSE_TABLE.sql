CREATE TABLE CREDIT_CARD_EXPENSE (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EXPENSE_DESCRIPTION VARCHAR(180) NOT NULL CHECK (EXPENSE_DESCRIPTION <> ''),
    EXPENSE_DATE TIMESTAMP NOT NULL,
    EXPENSE_CATEGORY BIGINT NOT NULL,
    CREDIT_CARD BIGINT NOT NULL,
    CREATED_BY BIGINT NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    FOREIGN KEY (CREATED_BY) REFERENCES PERSON (ID),
    FOREIGN KEY (CREDIT_CARD) REFERENCES CREDIT_CARD (ID),
    FOREIGN KEY (EXPENSE_CATEGORY) REFERENCES EXPENSE_CATEGORY (ID)
);