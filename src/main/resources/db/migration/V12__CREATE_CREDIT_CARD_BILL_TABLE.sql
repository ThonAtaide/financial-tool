CREATE TABLE CREDIT_CARD_BILL (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    REFERENCE_MONTH TIMESTAMP,
    CREDIT_CARD BIGINT NOT NULL,
    EXPENSE BIGINT NOT NULL,
    PAID_IN_FULL BOOLEAN NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    FOREIGN KEY (CREDIT_CARD) REFERENCES CREDIT_CARD (ID),
    FOREIGN KEY (EXPENSE) REFERENCES EXPENSE (ID),
    UNIQUE(CREDIT_CARD, REFERENCE_MONTH)
);