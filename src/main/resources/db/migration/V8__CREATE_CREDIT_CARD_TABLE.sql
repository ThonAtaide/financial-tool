CREATE TABLE CREDIT_CARD (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CARD_NAME VARCHAR(50) NOT NULL CHECK (CARD_NAME <> ''),
    STATEMENT_CLOSING_DAY INTEGER NOT NULL DEFAULT 10,
    DEFAULT_FIN_ACCOUNT BIGINT NOT NULL,
    CREATED_BY BIGINT NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    FOREIGN KEY (CREATED_BY) REFERENCES PERSON (ID),
    FOREIGN KEY (DEFAULT_FIN_ACCOUNT) REFERENCES FINANCIAL_ACCOUNT (ID)
);